    "class Vehicle:\n",
    "    \"\"\"Represents a generic vehicle.\"\"\"\n",
    "\n",
    "    def __init__(self, make, model, year, fuel_type):\n",
    "        self.__make = make\n",
    "        self.__model = model\n",
    "        self.__year = year\n",
    "        self.__fuel_type = fuel_type\n",
    "\n",
    "    def get_make(self):\n",
    "        return self.__make\n",
    "\n",
    "    def get_model(self):\n",
    "        return self.__model\n",
    "\n",
    "    def get_year(self):\n",
    "        return self.__year\n",
    "\n",
    "    def get_fuel_type(self):\n",
    "        return self.__fuel_type\n",
    "\n",
    "    def __str__(self):\n",
    "        return f\"{self.__year} {self.__make} {self.__model} ({self.__fuel_type})\"\n",
    "\n",
    "\n",
    "class Car(Vehicle):\n",
    "    \"\"\"A car is a specific type of Vehicle.\"\"\"\n",
    "\n",
    "    def __init__(self, make, model, year, fuel_type, num_doors):\n",
    "        # TODO: call the Vehicle constructor\n",
    "        super().__init__(make, model, year, fuel_type)\n",
    "        # store extra attribute\n",
    "        self.__num_doors = num_doors\n",
    "\n",
    "    def get_num_doors(self):\n",
    "        return self.__num_doors\n",
    "\n",
    "    def __str__(self):\n",
    "        # TODO: extend parent __str__ with door information\n",
    "        base = super().__str__()\n",
    "        return f\"{base} â€“ Car with {self.__num_doors} doors\"\n",
