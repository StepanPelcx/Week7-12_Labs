    "## 4. Inheritance: `Vehicle` (base class) and `Car` (subclass)\n",
    "\n",
    "In the slides you saw examples of **inheritance** using `Animal`, `Dog`, and `Cat`.\n",
    "We'll now do a similar thing with `Vehicle` and `Car`:\n",
    "\n",
    "- `Vehicle` will be a **general** class for any vehicle.\n",
    "- `Car` will be a **specialised** type of `Vehicle`.\n",
    "\n",
    "### Design\n",
    "\n",
    "- Class `Vehicle`:\n",
    "  - Private attributes: `__make`, `__model`, `__year`, `__fuel_type`\n",
    "  - Methods: getters + `__str__`\n",
    "- Class `Car(Vehicle)`:\n",
    "  - Extra attribute: `__num_doors`\n",
    "  - Uses `super().__init__(...)` to call the base constructor\n",
    "  - Overrides `__str__` to include number of doors\n",
    "\n",
    "### Task 4.1\n",
